package com.amtkxa.common.exception;

public class ReladomoConfigurationException extends RuntimeException {

    public ReladomoConfigurationException() {
        super();
    }

    public ReladomoConfigurationException(String message) {
        super(message);
    }

    public ReladomoConfigurationException(Throwable cause) {
        super(cause);
    }

    public ReladomoConfigurationException(String message, Throwable cause) {
        super(message, cause);
    }
}
